package model;

/**
 *
 * @author Admin
 */
public class Category {
    private int id,aid;
    private String name,describe;

    public Category() {
    }

    public Category(int id, int aid, String name, String describe) {
        this.id = id;
        this.aid = aid;
        this.name = name;
        this.describe = describe;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAid() {
        return aid;
    }

    public void setAid(int aid) {
        this.aid = aid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescribe() {
        return describe;
    }

    public void setDescribe(String describe) {
        this.describe = describe;
    }

    
    
}
